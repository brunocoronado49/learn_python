# String
cadena = "Hola soy un string"
print(cadena)

# Int
number = 30
print(number)

# Float
float = 30.5
print(float)

# Boolean
boleano = True 
print(boleano)

# List
list = [10, 20, 30, 40]
print(list)

# Tuples
tuple = (20, 30, 40, 55)
print(tuple)

 # Dictionaries
dic = {"Fransico": "Bruno"}
print(dic)

# Muestra por consola el tipo de dato
print(type(cadena))
print(type(number))
print(type(float))
print(type(boleano))
print(type(list))
print(type(tuple))
print(type(dic))
